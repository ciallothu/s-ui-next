import 'package:flutter/material.dart';

class AppLanguage {
  const AppLanguage(this.code, this.label);
  final String code;
  final String label;
}

class AppLocalizations {
  static const languages = <AppLanguage>[
    AppLanguage('en', 'English'),
    AppLanguage('zhHans', '简体中文'),
    AppLanguage('zhHant', '繁體中文'),
    AppLanguage('ja', '日本語'),
    AppLanguage('fr', 'Français'),
    AppLanguage('la', 'Latina'),
    AppLanguage('fa', 'فارسی'),
    AppLanguage('vi', 'Tiếng Việt'),
    AppLanguage('ru', 'Русский'),
  ];

  static const supportedLocales = <Locale>[
    Locale('en'),
    Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hans'),
    Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hant'),
    Locale('ja'),
    Locale('fr'),
    Locale('fa'),
    Locale('vi'),
    Locale('ru'),
  ];

  static Locale localeOf(String code) => switch (code) {
        'zhHans' => const Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hans'),
        'zhHant' => const Locale.fromSubtags(languageCode: 'zh', scriptCode: 'Hant'),
        'la' => const Locale('en'),
        _ => Locale(code),
      };

  static String bestMatch(Locale locale) {
    if (locale.languageCode == 'zh') {
      return locale.scriptCode == 'Hant' ? 'zhHant' : 'zhHans';
    }
    if (languages.any((item) => item.code == locale.languageCode)) return locale.languageCode;
    return 'en';
  }

  static String tr(String code, String key, {Map<String, Object?> args = const {}}) {
    final value = (_messages[code] ?? _messages['en'] ?? const {})[key] ?? _messages['en']?[key] ?? key;
    return args.entries.fold(value, (text, entry) => text.replaceAll('{${entry.key}}', entry.value?.toString() ?? ''));
  }

  static String fieldLabel(String code, String key) {
    final wireGuardLabel = _wireGuardFieldLabels[code]?[key];
    if (wireGuardLabel != null) return wireGuardLabel;
    final map = code == 'zhHans' || code == 'zhHant' ? _fieldLabelsZhHans : _fieldLabelsEn;
    return map[key] ?? _fieldLabelsEn[key] ?? key.replaceAll('_', ' ');
  }

  static String singularLabel(String code, String key) {
    final map = code == 'zhHans' || code == 'zhHant' ? _singularLabelsZhHans : _singularLabelsEn;
    return map[key] ?? _singularLabelsEn[key] ?? tr(code, 'editor.item');
  }
}

